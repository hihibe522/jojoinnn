<script>
import $ from "jquery";
export default {
    name: "FAQ",
    data() {
        return {
            accList: [],
            panelList: [],
        };
    },
    methods: {
        accordion: function() {
            var i;
            this.accList = $(".accordion");
            this.panelList = $(".panel");
            this.panelList.css({ maxHeight: "0px" });

            // click事件
            this.accList.click((e) => {
                this.accList.removeClass("active");
                $(e.target).addClass("active");
                var panel = $(e.target).next();
                //如果展開的話收合
                if (panel.css("maxHeight") !== "0px"){
                      panel.css({"maxHeight": "0px" });
                    $(e.target).find(".FAQ_pointDown").removeClass("FAQ_rotate");
                } else {
                    //點其中一個，其他的收
                    $(".panel").css({ "maxHeight": "0px" });
                    $(".FAQ_pointDown").removeClass("FAQ_rotate");
                    panel.css("maxHeight", `${panel.prop("scrollHeight") + 50}px`);
                    $(e.target).find(".FAQ_pointDown").addClass("FAQ_rotate");
                }
            });
        },
    },
    mounted() {
        this.accordion();
    },
}
</script>

